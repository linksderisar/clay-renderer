export default {
    '==': (first, second) => first == second,
    '===': (first, second) => first === second,
    '>': (first, second) => first > second,
    '<': (first, second) => first < second,
    '>=': (first, second) => first >= second,
    '<=': (first, second) => first <= second,

    '!=': (first, second) => first != second,
    '!==': (first, second) => first !== second,
};
